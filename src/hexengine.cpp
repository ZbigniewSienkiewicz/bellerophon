    void get_legal_king_moves_at(HBoard &board, const int index, std::vector<Move> &moves, const bool only_captures) {
        const Piece piece = board.board_array[index];
        if (piece.type != PieceType::King) return;

        for (int j = 0; j < MAX_KING_MOVES; ++j) {
            const int target = king_moves[index][j];
            if (target == -1) break;

            if (board.board_array[target].side == piece.side || board.board_array[target].type == PieceType::King) continue;
            if (only_captures && board.board_array[target].type == PieceType::Empty) continue;

            Move move = {index, target, board.board_array[target], PieceType::Empty, false, board.en_passant_square};
            if (!is_checked(board, move)) {
                moves.push_back(move);
            }
        }
    }

